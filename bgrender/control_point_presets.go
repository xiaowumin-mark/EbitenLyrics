package bgrender

// 文件说明：背景控制点预设集合。
// 主要职责：为不同视觉效果提供可直接使用的控制点布局。

type ControlPointConf struct {
	CX int
	CY int
	X  float64
	Y  float64
	UR float64
	VR float64
	UP float64
	VP float64
}

type ControlPointPreset struct {
	Width  int
	Height int
	Conf   []ControlPointConf
}

func cp(
	cx, cy int,
	x, y float64,
	ur, vr, up, vp float64,
) ControlPointConf {
	return ControlPointConf{
		CX: cx,
		CY: cy,
		X:  x,
		Y:  y,
		UR: ur,
		VR: vr,
		UP: up,
		VP: vp,
	}
}

func controlPointPreset(width, height int, conf ...ControlPointConf) ControlPointPreset {
	return ControlPointPreset{
		Width:  width,
		Height: height,
		Conf:   conf,
	}
}

